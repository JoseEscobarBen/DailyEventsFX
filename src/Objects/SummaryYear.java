
package Objects;

public class SummaryYear {
    private int number_year;
    private float summary;

    public SummaryYear(int number_year, float summary) {
        this.number_year = number_year;
        this.summary = summary;
    }

    public int getNumber_year() {
        return number_year;
    }

    public void setNumber_year(int number_year) {
        this.number_year = number_year;
    }

    public float getSummary() {
        return summary;
    }

    public void setSummary(float summary) {
        this.summary = summary;
    }
    
    
}
